    Le client doit effectuer son paiement lors de la commande. Les sommes versées ne pourront être considérées comme acomptes ou des arrhes. La totalité de l’encaissement sera effectué par DocumentsOfficiels.fr au moment de la validation de la commande.<br><br>
    L'intégralité des commandes sont payables en euros. L’acheteur dispose de l’ensemble des moyens de paiement via carte bancaire visés au sein du bon de commande.
    Dans le cadre de la lutte des fraudes sur Internet, les informations relatives à votre commande pourront être transmises à tout tiers pour vérification.<br><br>
    L’utilisateur dispose de 12 mois pour valider sa commande en envoyant les documents nécessaires. Passé ce délai, la commande sera considérée comme terminée et ne pourra plus faire l’objet d’un traitement ou d’un remboursement.
