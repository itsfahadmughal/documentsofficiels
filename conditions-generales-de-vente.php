    <h4>Taxes de carte grise</h4>
    Les taxes sont collectées par DocumentsOfficiels.fr pour le compte du Trésor Public. <br>Le montant est estimé lors de la commande et dépend des caractéristiques techniques du véhicule et du prix des chevaux fiscaux fixés par les régions chaque année.<br> Plus d’informations sur https://immatriculation.ants.gouv.fr/Vos-demarches/Cout-et-taxes-additionnelles
    <br><br>
    <h4>Confirmation et traitement de commande</h4>
    La validation d’une commande de carte grise et son traitement sont faits une fois le paiement et la réception des documents effectués.
    <br><br>
    <h4>Remboursement</h4>
    En cas de demande d’annulation par le client avant tout traitement lié à l’établissement de la commande par DocumentsOfficiels.fr et dans les 14 jours suivants la commande, le remboursement pourra être effectué intégralement.<br><br>
    
    Après 14 jours ou dès lors qu’un traitement a eu lieu sur la commande, la prestation de DocumentsOfficiels.fr ne pourra être remboursée. Seules les taxes de cartes grises pourront faire l’objet d’un remboursement.<br><br>
    
    Les frais de traitement liés à l’établissement de la commande sont caractérisés par la génération de la liste des documents à fournir, les documents pdf CERFA, les frais de gestion d’ouverture de dossier, la collecte et le contrôle des données Système d'Immatriculation des Véhicules (SIV) et le pré-contrôle de la conformité du dossier.<br><br>
    
    Si le véhicule ne peut être immatriculé car le dossier présente une anomalie (véhicule gagé, volé, pièces manquantes au dossier, titulaire différent avec l’enregistrement en préfecture, date modifiée sur la carte grise, incohérence de dates entre la cession et la carte grise…), l’intégralité de la prestation de DocumentsOfficiels.fr ne pourra être remboursée. Seules les taxes pour le Trésor Public feront l’objet d’un remboursement.
    <br><br>
    <h4>Livraison</h4>
    Dès réception de l'intégralité des pièces justificatives, DocumentsOfficiels.fr s’engage à traiter le dossier d’immatriculation dans les meilleurs délais. Le délai de traitement peut aller jusqu’à 60 jours, certains dossiers nécessitent un traitement par les services de l’Etat directement ou des processus de traitement différents.
    <br><br>
