<p>Le <strong>service SIV </strong>permet aux usagers de faire leurs démarches d&rsquo;immatriculation sans être dépendant de l&rsquo;administration. De nombreux acteurs ont accès au<strong> SIV en ligne </strong>pour offrir des prestations carte grise de qualité.</p>



<h2 id="1-nouveau-systeme-dimmatriculation-des-vehicules-siv-en-ligne">Nouveau système d’immatriculation des véhicules&nbsp;: SIV en ligne</h2>



<p>Le système d’immatriculation des véhicules a été mis en place pour remplacer le&nbsp;<a href="#/plaque-immatriculation/ancienne.html">fichier national d’immatriculation (FNI)</a>&nbsp;devenu obsolète et inadapté aux nouvelles normes européennes.</p>



<p>Le SIV a été mis en place progressivement, d’abord le 15 avril 2009 pour les véhicules neufs puis le 15 octobre 2009 pour les véhicules d’occasions. Le 15 avril 2010, soit un an après la mise en place du système, devait être le lancement du passage au SIV sur la base du volontariat mais cette date a été repoussée à une date ultérieure.</p>



<h2 id="2-changements-apportes-par-le-siv">Changements apportés par le SIV</h2>



<p>Grâce à de nombreux changements apportés au système d’immatriculation français, le <a href="#/siv/fichier-siv">fichier SIV</a> se veut plus simple, plus sur et plus performant.</p>



<p>Voici la liste des&nbsp;<strong>principales améliorations du système d’immatriculation</strong>&nbsp;:</p>



<ul><li>Une seule agence centralise les demandes d’immatriculation, c’est l’Agence Nationale des Titres Sécurisés. Elle s’occupe également des passeports français et prochainement des permis de conduire et de la carte nationale d’identité.</li><li>Le&nbsp;<a href="#/carte-grise/certificat-immatriculation.html">certificat d’immatriculation</a>&nbsp;est imprimé et envoyé par l’Imprimerie Nationale. Il s’agit d’une double sécurité puisque le document n’est plus envoyé vierge aux différentes préfectures de France et l’adresse des usagers est vérifiée grâce à l’envoi de la carte grise en recommandé au domicile du titulaire.</li><li>La carte grise comprend plus d’informations. Elle affiche de nouvelles sécurités. Le nom du document a également évolué, on parle de certificat d’immatriculation. Ces deux mesures visent à harmoniser tous les documents d’immatriculation des pays de l’Union Européenne.</li><li>Le numéro des plaques d’immatriculation s’harmonise à l’échelle nationale sous la forme&nbsp;: AA-123-AA. L’attribution du numéro d’immatriculation est définitive pour chaque véhicule (conservé à vie) et suit un ordre chronologique national</li><li>Depuis l’arrivée du SIV, les automobilistes ont la possibilité de confier leur dossier de demande d’immatriculation à un professionnel habilité. Ce <a href="#/carte-grise/prestataire-prive-carte-grise">prestataire privé carte grise</a> enregistre directement les demandes reçues dans l’application SIV. Ce qui en soi est une alternative des plus envisageables, surtout depuis la fermeture des guichets des préfectures engendrant des saturations sur le site de l’ANTS</li></ul>



<h2 id="3-obtention-du-certificat-dimmatriculation-services-siv">Obtention du certificat d’immatriculation : services SIV</h2>



<p>La <strong>grande nouveauté du SIV</strong> réside dans les nouveaux moyens d’obtention de la carte grise. En effet, il n’est plus obligatoire de traiter avec l&rsquo;État pour immatriculer un véhicule. Les automobilistes ont le désormais le choix entre&nbsp;:</p>



<ul><li>Traiter avec l&rsquo;ANTS, ce qui peut être parfois compliqué en raison des contraintes du service</li><li>Faire sa <strong>carte grise en ligne</strong>&nbsp;: vous pouvez commander votre carte grise par internet sur notre site, il suffit de renseigner les caractéristiques du véhicule pour obtenir le prix de sa&nbsp;<a href="#/carte-grise.html">carte grise</a>. documentsofficiels se charge de toutes les démarches d’immatriculation à la place de l’automobiliste</li><li>Auprès d’un professionnel de l’automobile habilité&nbsp;</li></ul>



<p>Le titulaire du véhicule doit constituer un dossier d’immatriculation complet et le remettre à la société habilité à immatriculer un véhicule.<br>Dès lors un <strong>Certificat Provisoire d’Immatriculation (CPI)</strong>, valable un mois, est fourni. Ce CPI déclenche automatiquement l’édition du Certificat d’Immatriculation par l’Imprimerie Nationale. Sous 3 à 5 jours ouvrés, le titulaire du véhicule reçoit la carte grise définitive par pli sécurisé.</p>



<h2 id="4-service-siv-en-ligne-quelles-sont-les-demarches-a-suivre">Service SIV en ligne : quelles sont les démarches à suivre ?</h2>



<p>Normalement, seuls les véhicules neufs (depuis le 15 avril 2009) et ceux encore enregistrés dans l’ancien système FNI (depuis le 15 octobre 2009) peuvent prétendre à une immatriculation SIV. En effet pour l’instant le changement d’immatriculation ne peut se faire de manière spontanée. Pour avoir un&nbsp;<strong>numéro SIV</strong>, il faut entamer une démarche d’immatriculation telle qu’un changement d’adresse, une demande de première immatriculation, une demande de duplicata, etc.</p>



<h2 id="5-service-siv-pour-qui%e2%80%89">Service SIV&nbsp;: pour qui ?</h2>



<p>Le service SIV/<strong>SIV en ligne </strong>s’adresse tout particulièrement aux professionnels, mais également à d’autres institutions. Il en est ainsi des professionnels du commerce automobile et des cyclomoteurs, constructeurs automobiles, centres VHU (démolisseurs et broyeurs). Sans oublier les experts automobiles, sociétés de location de véhicules et huissiers. Le SIV en ligne est aussi consultable par d’autres entités. À savoir les services des douanes, assureurs, services antiterroristes, imprimerie nationale et organismes de crédit.</p>



<h2 id="6-faq-siv-en-ligne">FAQ SIV en ligne</h2>



<div class="schema-faq wp-block-yoast-faq-block"><div class="schema-faq-section"><strong class="schema-faq-question">Je suis victime d’usurpation de plaques d’immatriculation, vais-je recevoir un nouveau numéro d’immatriculation SIV ?</strong> <p class="schema-faq-answer">Oui, c’est un cas exceptionnel qui nécessite l’attribution d’un nouveau numéro d’immatriculation SIV.</p> </div> <div class="schema-faq-section"><strong class="schema-faq-question">Les anciens numéros d’immatriculation sont-ils voués à disparaître ?</strong> <p class="schema-faq-answer">Oui, mais cela risque de prendre du temps. Les véhicules avec une immatriculation FNI qui ne font pas l&rsquo;objet d&rsquo;un changement conservent leur numéro. Un basculement obligatoire était prévu pour 2020, mais cette disposition a été annulée.</p> </div> <div class="schema-faq-section"><strong class="schema-faq-question">Combien coûte un changement d&rsquo;immatriculation FNI en SIV ?</strong> <p class="schema-faq-answer">Cette procédure n&rsquo;engendre pas de frais supplémentaires. Mieux encore, avoir une carte grise FNI permet parfois de faire des économies. Par exemple, si vous devez faire une demande de duplicata pour un ancien certificat d&rsquo;immatriculation, la procédure ne coûte que 2,76 euros.</p> </div> <div class="schema-faq-section"><strong class="schema-faq-question">Comment avoir accès au SIV en ligne (fichier national des immatriculations) ?</strong> <p class="schema-faq-answer">Il faut introduire une demande d&rsquo;habilitation auprès du ministère de l&rsquo;Intérieur.</p> </div> <div class="schema-faq-section"><strong class="schema-faq-question">Comment connaître la dernière immatriculation SIV ?</strong> <p class="schema-faq-answer">L&rsquo;Etat ne fournit pas cette information. Des sites privés proposent néanmoins un simulateur qui permet de savoir plus ou moins où nous en sommes concernant la <a href="#/siv/derniere-immatriculation-siv">dernière immatriculation SIV attribuée</a>.</p> </div> <div class="schema-faq-section"><strong class="schema-faq-question">Comment changer une plaque d&rsquo;immatriculation FNI en SIV ?</strong> <p class="schema-faq-answer">La conversion spontanée n&rsquo;est pas possible en ce moment. Seule une démarche permet de changer une plaque d&rsquo;immatriculation FNI en SIV (demande de duplicata, changement d&rsquo;adresse, etc.).</p> </div> </div>
